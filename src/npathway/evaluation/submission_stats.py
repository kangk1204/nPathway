"""Submission-grade statistical package builder for expanded benchmark runs."""

from __future__ import annotations

import re
from dataclasses import dataclass
from pathlib import Path
from typing import Literal

import numpy as np
import pandas as pd

TrackType = Literal["de_novo", "reference_guided", "all"]


class GateFailure(RuntimeError):
    """Raised when a submission gate fails."""


@dataclass(frozen=True)
class SubmissionStatsConfig:
    """Configuration for building submission statistics artifacts."""

    min_seeds_per_dataset: int = 5
    min_non_ties: int = 8
    fdr_alpha: float = 0.05
    prioritize_track: TrackType = "de_novo"
    prefer_primary_stats: bool = True
    require_fair_methods: bool = True
    require_npathway_supported_claim: bool = True


@dataclass(frozen=True)
class SubmissionStatsResult:
    """Artifacts generated by submission stats package builder."""

    support_path: Path
    risk_path: Path
    npathway_support_path: Path
    npathway_risk_path: Path
    npathway_guardrails_path: Path
    summary_path: Path
    n_candidates: int
    n_supported: int
    n_risks: int
    n_npathway_supported: int
    readiness_pass: bool
    used_stats_file: str


def _read_csv(path: Path) -> pd.DataFrame:
    if not path.exists():
        raise GateFailure(f"Required file not found: {path}")
    return pd.read_csv(path)


def _require_columns(df: pd.DataFrame, required: set[str], table_name: str) -> None:
    missing = sorted(required - set(df.columns))
    if missing:
        raise GateFailure(
            f"Missing required columns in {table_name}: {', '.join(missing)}"
        )


def _as_bool_series(series: pd.Series) -> pd.Series:
    return (
        series.astype(str)
        .str.strip()
        .str.lower()
        .map(
            {
                "true": True,
                "1": True,
                "yes": True,
                "y": True,
                "false": False,
                "0": False,
                "no": False,
                "n": False,
            }
        )
        .fillna(False)
    )


def _choose_stats_table(tables_dir: Path, prefer_primary: bool) -> tuple[pd.DataFrame, str]:
    primary_path = tables_dir / "expanded_primary_wilcoxon_vs_cnmf.csv"
    full_path = tables_dir / "expanded_wilcoxon_vs_cnmf.csv"

    if prefer_primary and primary_path.exists():
        return pd.read_csv(primary_path), primary_path.name
    if full_path.exists():
        return pd.read_csv(full_path), full_path.name
    if primary_path.exists():
        return pd.read_csv(primary_path), primary_path.name
    raise GateFailure(
        "No stats table found. Expected one of "
        "'expanded_primary_wilcoxon_vs_cnmf.csv' or "
        "'expanded_wilcoxon_vs_cnmf.csv'."
    )


def _ci_direction_ok(
    alternative: str,
    ci_low: float,
    ci_high: float,
) -> bool:
    if not np.isfinite(ci_low) or not np.isfinite(ci_high):
        return False
    if alternative == "greater":
        return ci_low > 0.0
    if alternative == "less":
        return ci_high < 0.0
    return False


def _effect_direction_ok(alternative: str, delta_mean: float) -> bool:
    if not np.isfinite(delta_mean):
        return False
    if alternative == "greater":
        return delta_mean > 0.0
    if alternative == "less":
        return delta_mean < 0.0
    return False


def _validate_seed_gate(seed_summary: pd.DataFrame, min_seeds: int) -> pd.DataFrame:
    counts = (
        seed_summary.groupby("dataset", as_index=False)["seed"]
        .nunique()
        .rename(columns={"seed": "n_unique_seeds"})
    )
    failing = counts[counts["n_unique_seeds"] < min_seeds]
    if not failing.empty:
        details = ", ".join(
            f"{row.dataset}:{int(row.n_unique_seeds)}"
            for row in failing.itertuples(index=False)
        )
        raise GateFailure(
            f"Min-seed gate failed (required >= {min_seeds}): {details}"
        )
    return counts


def _merge_track(stats_df: pd.DataFrame, seed_summary: pd.DataFrame) -> pd.DataFrame:
    track_map = (
        seed_summary[["method", "track"]]
        .drop_duplicates()
        .groupby("method", as_index=False)
        .first()
    )
    merged = stats_df.merge(
        track_map,
        on="method",
        how="left",
        suffixes=("", "_seed"),
    )
    if "track_seed" in merged.columns:
        if "track" in merged.columns:
            merged["track"] = merged["track"].where(
                merged["track"].notna(),
                merged["track_seed"],
            )
            merged = merged.drop(columns=["track_seed"])
        else:
            merged = merged.rename(columns={"track_seed": "track"})
    merged["track"] = merged["track"].fillna("unknown")
    return merged


def _merge_fairness(stats_df: pd.DataFrame, fairness_df: pd.DataFrame) -> pd.DataFrame:
    _require_columns(
        fairness_df,
        required={"method", "track", "shared_input_pipeline", "seed_controlled"},
        table_name="expanded_fairness_manifest.csv",
    )
    fairness = (
        fairness_df[["method", "shared_input_pipeline", "seed_controlled"]]
        .drop_duplicates(subset=["method"])
        .copy()
    )
    fairness["shared_input_pipeline"] = _as_bool_series(
        fairness["shared_input_pipeline"]
    )
    fairness["seed_controlled"] = _as_bool_series(fairness["seed_controlled"])
    fairness["fairness_pass"] = (
        fairness["shared_input_pipeline"] & fairness["seed_controlled"]
    )

    merged = stats_df.merge(fairness, on="method", how="left")
    merged["shared_input_pipeline"] = merged["shared_input_pipeline"].fillna(False)
    merged["seed_controlled"] = merged["seed_controlled"].fillna(False)
    merged["fairness_pass"] = merged["fairness_pass"].fillna(False)
    return merged


def _filter_track(df: pd.DataFrame, prioritize_track: TrackType) -> pd.DataFrame:
    if prioritize_track == "all":
        return df.copy()
    sub = df[df["track"] == prioritize_track].copy()
    if sub.empty:
        raise GateFailure(
            f"No rows available for prioritize_track='{prioritize_track}'."
        )
    return sub


def _filter_fairness(df: pd.DataFrame, require_fair_methods: bool) -> pd.DataFrame:
    if not require_fair_methods:
        return df.copy()
    sub = df[df["fairness_pass"]].copy()
    if sub.empty:
        raise GateFailure(
            "No rows pass fairness gate (shared_input_pipeline=True and seed_controlled=True)."
        )
    return sub


def _classify_rows(
    stats_df: pd.DataFrame,
    *,
    min_pairs: int,
    min_non_ties: int,
    fdr_alpha: float,
) -> pd.DataFrame:
    out = stats_df.copy()
    out["n_pairs"] = pd.to_numeric(out["n_pairs"], errors="coerce")
    out["n_non_ties"] = pd.to_numeric(out["n_non_ties"], errors="coerce")
    out["fdr_bh"] = pd.to_numeric(out["fdr_bh"], errors="coerce")
    out["delta_mean"] = pd.to_numeric(out["delta_mean"], errors="coerce")
    out["delta_ci95_low"] = pd.to_numeric(out["delta_ci95_low"], errors="coerce")
    out["delta_ci95_high"] = pd.to_numeric(out["delta_ci95_high"], errors="coerce")

    out["fdr_pass"] = out["fdr_bh"].apply(
        lambda x: bool(np.isfinite(x) and float(x) <= fdr_alpha)
    )
    out["n_pairs_pass"] = out["n_pairs"].apply(
        lambda x: bool(np.isfinite(x) and int(x) >= min_pairs)
    )
    out["n_non_ties_positive"] = out["n_non_ties"].apply(
        lambda x: bool(np.isfinite(x) and int(x) > 0)
    )
    out["n_non_ties_pass"] = out["n_non_ties"].apply(
        lambda x: bool(np.isfinite(x) and int(x) >= min_non_ties)
    )
    out["ci_direction_pass"] = [
        _ci_direction_ok(str(a), float(lo), float(hi))
        for a, lo, hi in zip(
            out["alternative"],
            out["delta_ci95_low"],
            out["delta_ci95_high"],
        )
    ]
    out["effect_direction_pass"] = [
        _effect_direction_ok(str(a), float(d))
        for a, d in zip(out["alternative"], out["delta_mean"])
    ]
    out["is_npathway"] = out["method"].astype(str).str.lower().str.startswith(
        "npathway"
    )
    out["supported"] = (
        out["fdr_pass"]
        & out["n_pairs_pass"]
        & out["n_non_ties_pass"]
        & out["n_non_ties_positive"]
        & out["ci_direction_pass"]
        & out["effect_direction_pass"]
    )

    risk_reason: list[str] = []
    risk_level: list[str] = []
    for row in out.itertuples(index=False):
        reasons: list[str] = []
        if not bool(row.n_pairs_pass):
            reasons.append("insufficient_pairs")
        if not bool(row.n_non_ties_positive):
            reasons.append("no_non_ties")
        elif not bool(row.n_non_ties_pass):
            reasons.append("low_non_ties")
        if not bool(row.fdr_pass):
            reasons.append("fdr_fail")
        if not bool(row.ci_direction_pass):
            reasons.append("ci_direction_fail")
        if not bool(row.effect_direction_pass):
            reasons.append("effect_direction_fail")
        if not reasons:
            risk_reason.append("none")
            risk_level.append("none")
            continue

        if (
            "fdr_fail" not in reasons
            and "no_non_ties" not in reasons
            and "low_non_ties" not in reasons
            and (
                "ci_direction_fail" in reasons or "effect_direction_fail" in reasons
            )
        ):
            level = "high"
        elif any(
            reason in reasons
            for reason in ("insufficient_pairs", "no_non_ties", "low_non_ties")
        ):
            level = "medium"
        else:
            level = "low"
        risk_reason.append(";".join(reasons))
        risk_level.append(level)
    out["risk_reason"] = risk_reason
    out["risk_level"] = risk_level
    return out


def _claim_id(dataset: str, method: str, metric: str) -> str:
    token = f"{dataset}__{method}__{metric}"
    return re.sub(r"[^A-Za-z0-9_.-]+", "_", token)


def _build_guardrails(candidate_df: pd.DataFrame) -> pd.DataFrame:
    rows: list[dict[str, object]] = []
    for row in candidate_df.itertuples(index=False):
        dataset = str(row.dataset)
        method = str(row.method)
        reference = str(getattr(row, "reference", "cNMF"))
        metric = str(row.metric)
        passes_gate = bool(row.supported)
        reason = str(row.risk_reason)
        delta_mean = float(row.delta_mean)
        ci_low = float(row.delta_ci95_low)
        ci_high = float(row.delta_ci95_high)
        q = float(row.fdr_bh)

        if passes_gate:
            allowed = (
                f"{method} improved {metric} versus {reference} in {dataset} "
                f"(delta={delta_mean:.4g}, 95% CI [{ci_low:.4g}, {ci_high:.4g}], q={q:.4g})."
            )
        else:
            allowed = (
                f"No directional superiority claim for {method} on {metric} in {dataset}; "
                f"classified as {reason}."
            )
        forbidden = (
            f"{method} is superior to {reference} on {metric} in {dataset}."
        )

        rows.append(
            {
                "claim_id": _claim_id(dataset, method, metric),
                "dataset": dataset,
                "method": method,
                "reference": reference,
                "metric": metric,
                "track": str(row.track),
                "n_pairs": int(row.n_pairs),
                "n_non_ties": int(row.n_non_ties),
                "delta_mean": delta_mean,
                "delta_ci95_low": ci_low,
                "delta_ci95_high": ci_high,
                "p_value": float(row.p_value),
                "fdr_bh": q,
                "passes_gate": passes_gate,
                "risk_reason": reason,
                "risk_level": str(row.risk_level),
                "allowed_claim_text": allowed,
                "forbidden_claim_text": forbidden,
            }
        )
    return pd.DataFrame(rows)


def _write_summary(
    *,
    summary_path: Path,
    config: SubmissionStatsConfig,
    used_stats_file: str,
    seed_counts: pd.DataFrame,
    candidate_df: pd.DataFrame,
    support_df: pd.DataFrame,
    risk_df: pd.DataFrame,
    npathway_candidate_df: pd.DataFrame,
    npathway_support_df: pd.DataFrame,
    readiness_pass: bool,
) -> None:
    lines: list[str] = []
    lines.append("# Submission Readiness Summary")
    lines.append("")
    lines.append("## Configuration")
    lines.append(f"- prioritize_track: `{config.prioritize_track}`")
    lines.append(f"- min_seeds_per_dataset: `{config.min_seeds_per_dataset}`")
    lines.append(f"- min_non_ties: `{config.min_non_ties}`")
    lines.append(f"- fdr_alpha: `{config.fdr_alpha}`")
    lines.append(f"- require_fair_methods: `{config.require_fair_methods}`")
    lines.append(
        "- require_npathway_supported_claim: "
        f"`{config.require_npathway_supported_claim}`"
    )
    lines.append(f"- stats_source: `{used_stats_file}`")
    lines.append("")
    lines.append("## Gate Status")
    lines.append("- required_files_columns: `PASS`")
    lines.append("- min_seeds_per_dataset: `PASS`")
    lines.append(
        f"- fairness_filter: `{'PASS' if config.require_fair_methods else 'SKIP'}`"
    )
    lines.append("- min_non_ties_per_comparison: `PASS`")
    lines.append(
        f"- readiness_verdict: `{'PASS' if readiness_pass else 'FAIL'}`"
    )
    lines.append("")
    lines.append("## Dataset Seed Counts")
    for row in seed_counts.itertuples(index=False):
        lines.append(f"- {row.dataset}: `{int(row.n_unique_seeds)}` seeds")
    lines.append("")
    lines.append("## Candidate/Support/Risk Counts")
    lines.append(f"- candidates: `{len(candidate_df)}`")
    lines.append(f"- supported_claims: `{len(support_df)}`")
    lines.append(f"- risk_claims: `{len(risk_df)}`")
    lines.append(f"- npathway_candidates: `{len(npathway_candidate_df)}`")
    lines.append(f"- npathway_supported_claims: `{len(npathway_support_df)}`")
    lines.append(
        "- untestable_n_non_ties_eq_0: "
        f"`{int((candidate_df['n_non_ties'] == 0).sum())}`"
    )
    lines.append(
        "- power_mean_fpr_untestable_n_non_ties_eq_0: "
        f"`{int(((candidate_df['metric'] == 'power_mean_fpr') & (candidate_df['n_non_ties'] == 0)).sum())}`"
    )
    lines.append("")
    lines.append("## Top Supported Claims (up to 10)")
    if support_df.empty:
        lines.append("- none")
    else:
        top = support_df.sort_values(
            ["fdr_bh", "delta_mean"],
            ascending=[True, False],
        ).head(10)
        for row in top.itertuples(index=False):
            lines.append(
                "- "
                f"{row.dataset} | {row.method} | {row.metric} | "
                f"delta={float(row.delta_mean):.4g} "
                f"[{float(row.delta_ci95_low):.4g}, {float(row.delta_ci95_high):.4g}] "
                f"q={float(row.fdr_bh):.4g}"
            )
    lines.append("")
    lines.append("## Top Risks (up to 10)")
    if risk_df.empty:
        lines.append("- none")
    else:
        top_risk = risk_df.sort_values(
            ["risk_level", "fdr_bh"],
            ascending=[True, True],
        ).head(10)
        for row in top_risk.itertuples(index=False):
            q = float(row.fdr_bh) if np.isfinite(row.fdr_bh) else float("nan")
            lines.append(
                "- "
                f"{row.dataset} | {row.method} | {row.metric} | "
                f"risk={row.risk_level} ({row.risk_reason}) | q={q:.4g}"
            )
    summary_path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def build_submission_stats_package(
    *,
    tables_dir: Path,
    out_dir: Path,
    config: SubmissionStatsConfig,
) -> SubmissionStatsResult:
    """Build submission-grade stats package from expanded benchmark outputs."""
    seed_summary = _read_csv(tables_dir / "expanded_seed_summary.csv")
    _read_csv(tables_dir / "expanded_dataset_provenance.csv")
    fairness_manifest = _read_csv(tables_dir / "expanded_fairness_manifest.csv")
    stats_df, used_stats_file = _choose_stats_table(
        tables_dir,
        prefer_primary=config.prefer_primary_stats,
    )

    _require_columns(
        seed_summary,
        required={"dataset", "seed", "method", "track"},
        table_name="expanded_seed_summary.csv",
    )
    _require_columns(
        stats_df,
        required={
            "dataset",
            "method",
            "metric",
            "alternative",
            "n_pairs",
            "n_non_ties",
            "delta_mean",
            "delta_ci95_low",
            "delta_ci95_high",
            "p_value",
            "fdr_bh",
        },
        table_name=used_stats_file,
    )

    seed_counts = _validate_seed_gate(
        seed_summary,
        min_seeds=config.min_seeds_per_dataset,
    )
    stats_with_track = _merge_track(stats_df, seed_summary)
    stats_with_fairness = _merge_fairness(stats_with_track, fairness_manifest)
    focused = _filter_track(stats_with_fairness, config.prioritize_track)
    focused = _filter_fairness(focused, config.require_fair_methods)
    classified = _classify_rows(
        focused,
        min_pairs=config.min_seeds_per_dataset,
        min_non_ties=config.min_non_ties,
        fdr_alpha=config.fdr_alpha,
    )

    support_df = classified[classified["supported"]].copy()
    risk_df = classified[~classified["supported"]].copy()
    risk_df = risk_df[risk_df["risk_reason"] != "none"].copy()

    npathway_candidate_df = classified[classified["is_npathway"]].copy()
    npathway_support_df = npathway_candidate_df[npathway_candidate_df["supported"]].copy()
    npathway_risk_df = npathway_candidate_df[~npathway_candidate_df["supported"]].copy()
    npathway_risk_df = npathway_risk_df[npathway_risk_df["risk_reason"] != "none"].copy()

    has_supported = len(support_df) > 0
    if config.require_npathway_supported_claim:
        has_supported = len(npathway_support_df) > 0

    high_direction_conflict = bool(
        (
            classified["fdr_pass"]
            & (~classified["ci_direction_pass"] | ~classified["effect_direction_pass"])
        ).any()
    )
    readiness_pass = bool(has_supported and not high_direction_conflict)

    npathway_guardrails_df = _build_guardrails(npathway_candidate_df)

    out_dir.mkdir(parents=True, exist_ok=True)
    support_path = out_dir / "claim_support_matrix.csv"
    risk_path = out_dir / "claim_risk_matrix.csv"
    npathway_support_path = out_dir / "npathway_claim_support_matrix.csv"
    npathway_risk_path = out_dir / "npathway_claim_risk_matrix.csv"
    npathway_guardrails_path = out_dir / "npathway_claim_guardrails.csv"
    summary_path = out_dir / "submission_readiness_summary.md"

    support_df.to_csv(support_path, index=False)
    risk_df.to_csv(risk_path, index=False)
    npathway_support_df.to_csv(npathway_support_path, index=False)
    npathway_risk_df.to_csv(npathway_risk_path, index=False)
    npathway_guardrails_df.to_csv(npathway_guardrails_path, index=False)
    _write_summary(
        summary_path=summary_path,
        config=config,
        used_stats_file=used_stats_file,
        seed_counts=seed_counts,
        candidate_df=classified,
        support_df=support_df,
        risk_df=risk_df,
        npathway_candidate_df=npathway_candidate_df,
        npathway_support_df=npathway_support_df,
        readiness_pass=readiness_pass,
    )

    return SubmissionStatsResult(
        support_path=support_path,
        risk_path=risk_path,
        npathway_support_path=npathway_support_path,
        npathway_risk_path=npathway_risk_path,
        npathway_guardrails_path=npathway_guardrails_path,
        summary_path=summary_path,
        n_candidates=len(classified),
        n_supported=len(support_df),
        n_risks=len(risk_df),
        n_npathway_supported=len(npathway_support_df),
        readiness_pass=readiness_pass,
        used_stats_file=used_stats_file,
    )
